function getValue(object, path) {
  return path.split(".").reduce((current, part) => current && current[part], object);
}

function applyLanguage(lang) {
  const current = translations[lang] || translations.en;
  const pageKey = document.body.dataset.page || "index";

  document.documentElement.lang = lang === "fa" ? "fa" : lang;
  document.documentElement.dir = lang === "fa" ? "rtl" : "ltr";
  document.body.classList.toggle("rtl", lang === "fa");
  document.title = current.titles[pageKey] || current.titles.index;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getValue(current, element.dataset.i18n);
    if (typeof value === "string") {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const value = getValue(current, element.dataset.i18nPlaceholder);
    if (typeof value === "string") {
      element.setAttribute("placeholder", value);
    }
  });

  const selector = document.getElementById("language-select");
  if (selector) {
    selector.value = lang;
  }
}

document.addEventListener("DOMContentLoaded", () => {
  const selector = document.getElementById("language-select");
  const savedLanguage = localStorage.getItem("portfolio-language") || "en";
  applyLanguage(savedLanguage);

  if (selector) {
    selector.addEventListener("change", (event) => {
      const selectedLanguage = event.target.value;
      localStorage.setItem("portfolio-language", selectedLanguage);
      applyLanguage(selectedLanguage);
    });
  }
});
